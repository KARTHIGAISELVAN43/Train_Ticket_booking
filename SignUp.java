     private boolean validatePhone(){
        String val = mobileno.getText().toString().trim();
        String validation = "^[0-9]{10}$";
        if(!val.matches(validation)){
                JOptionPane.showMessageDialog(SignUp.this, "Only 10 digits number is valid!", "PhoneNo Error", HEIGHT);
                return false;
        }
        else if(val.isEmpty()){
            JOptionPane.showMessageDialog(SignUp.this, "Field can't be empty", "PhoneNo Error", HEIGHT);
            return false;
        }
        else
        {
            return true;
        }
        }
     private boolean validateAddress(){
        String val = Address.getText().toString().trim();
        String validation = "^[#.0-9a-zA-Z\\s,-]+$";
        if(val.isEmpty()){
            JOptionPane.showMessageDialog(SignUp.this, "Field can't be empty", "AddressError", HEIGHT);
            return false;
        }
        else if(!val.matches(validation)){
            JOptionPane.showMessageDialog(SignUp.this,"Invalid format. Format should be #1, North Street, Chennai - 11");
            return false;
        }
        else
        {
            return true;
        }
        }
     

